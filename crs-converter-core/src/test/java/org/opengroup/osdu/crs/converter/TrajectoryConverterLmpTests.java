    private void compareAllSamples(List<TrajectoryStationOut> expected, ConvertTrajectoryResponse response) {
        int j = 0;
        for (int i = 0; i < response.getStations().size(); i++) {
            if (response.getStations().get(i).isOriginal()) {
                String message = String.format("At MD=%f index=%d", expected.get(j).getMd(), i);
                double dx = expected.get(j).getPoint().getX() - response.getStations().get(i).getPoint().getX();
                double dy = expected.get(j).getPoint().getY() - response.getStations().get(i).getPoint().getY();
                double dist = Math.sqrt(dx * dx + dy * dy);
                assertEquals(message, expected.get(j).getPoint().getX(), response.getStations().get(i).getPoint().getX(), DELTA_L);
                assertEquals(message, expected.get(j).getPoint().getY(), response.getStations().get(i).getPoint().getY(), DELTA_L);
                assertEquals(message, expected.get(j).getPoint().getZ(), response.getStations().get(i).getPoint().getZ(), DELTA_L);
                assertEquals(message, expected.get(j).getMd(), response.getStations().get(i).getMd(), 10e-10);
                assertEquals(message, expected.get(j).getInclination(), response.getStations().get(i).getInclination(), 10e-10);
                azimuthIsEqualEnough(expected.get(j).getAzimuthTN(), response.getStations().get(i).getAzimuthTN());
                //azimuthIsEqualEnough(expected[i][AZIM_GN], response.getStations().get(i).getAzimuthGN());
                // if (dist > 0.02) System.out.println(String.format("%3d Distance: %f", i, dist));
                j++;
            }
        }
    }

