    static void azimuthIsEqualEnough(double expected, double actual) {
        if (Double.isNaN(actual)) fail("Azimuth is NaN");
        double deltaAngle = expected - actual;
        if (deltaAngle < -359) deltaAngle += 360.0;
        if (deltaAngle > 359) deltaAngle -= 360.0;
        assertEquals(0.0, deltaAngle, DELTA_A);
    }

    private void compareAllOriginalSamples(double[][] expected, ConvertTrajectoryResponse response) {
        int j = 0;
        for (int i = 0; i < response.getStations().size(); i++) {
            if (response.getStations().get(i).isOriginal()) {
                assertEquals(expected[j][DX_TN], response.getStations().get(i).getDxTN(), DELTA_L);
                assertEquals(expected[j][DY_TN], response.getStations().get(i).getDyTN(), DELTA_L);
                assertEquals(expected[j][X], response.getStations().get(i).getPoint().getX(), DELTA_L);
                assertEquals(expected[j][Y], response.getStations().get(i).getPoint().getY(), DELTA_L);
                assertEquals(expected[j][Z], response.getStations().get(i).getPoint().getZ(), DELTA_L);
                assertEquals(expected[j][MD], response.getStations().get(i).getMd(), 10e-10);
                assertEquals(expected[j][INCL], response.getStations().get(i).getInclination(), 10e-10);
                assertEquals(expected[j][DLS], response.getStations().get(i).getDls(), DELTA_L);
                azimuthIsEqualEnough(expected[j][AZIM_TN], response.getStations().get(i).getAzimuthTN());
                azimuthIsEqualEnough(expected[j][AZIM_GN], response.getStations().get(i).getAzimuthGN());
                j++;
            }
        }
    }

