    static DpsHeaders getDpsHeaders() {
        DpsHeaders headers = new DpsHeaders();
        String value = "Bearer " + System.getProperty("BEARER_TOKEN", System.getenv("BEARER_TOKEN"));
        if (!Config.isNullOrEmpty(value)) headers.getHeaders().put("Authorization", value);
        headers.getHeaders().put("Content-Type", "application/json");
        headers.addCorrelationIdIfMissing();
        return headers;
    }
